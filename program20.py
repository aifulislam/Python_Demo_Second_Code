#Range function-----------

num = list(range(10))
print("list : ",num)
print("Index value : ",num[3])

num = list(range(5,10))
print("Starting and End : ",num)

num = list(range(2,20,2))
print("Even : ",num)

num = list(range(1,20,2))
print("Odd : ",num)

num = list(range(10,100,10))
print("Even : ",num)

