#Area of any shape-------

base = float(input("Enter any base : "))
height = float(input("Enter any height : "))

area = 0.5 * base * height
print("Area of triangle : ",area)

#Radius-------
radius = float(input("Enter radius : "))
area = 3.1416 * radius *radius
print("Area of Radius : ",area)

#Rectangle-----------
width = float(input("Enter your width : "))
heigh = float(input("Enter your weigh : "))

rectangle = width * heigh
print("Area of Rectangle : ",rectangle)

#Squre-----------
value = float(input("Enter your width : "))
squre = value * value
print("Area of Squre : ",squre)
