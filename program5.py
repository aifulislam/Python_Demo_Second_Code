#Type Casting-------

num1 = float(input("Enter your First number : "))
num2 = float(input("Enter your second number : "))

result = num1 + num2
print("Result is : ",result)

result = num1 - num2
print("Result is : ",result)

result = num1 * num2
print("Result is : ",result)

result = num1 / num2
print("Result is : ",result)

result = num1 % num2
print("Result is : ",result)
